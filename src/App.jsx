function getBase(countryBirth, countryNow, sex) {
  const idx = sex === "m" ? 0 : 1;
  const birthData = COUNTRIES[countryBirth];
  if (!birthData) return null;
  if (!countryNow || countryNow === countryBirth) return birthData[idx];
  const nowData = COUNTRIES[countryNow];
  if (!nowData) return birthData[idx];
  // Иммигрант: 60% от страны проживания + 40% от страны рождения
  return 0.6 * nowData[idx] + 0.4 * birthData[idx];
}

