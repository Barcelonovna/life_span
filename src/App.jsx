function BMIInput({ val, onChange }) {
  const [mode, setMode] = useState("hw");
  const [ht, setHt] = useState(""); const [wt, setWt] = useState("");
  useEffect(() => {
    if (mode === "hw" && ht && wt) {
      const h = parseFloat(ht) / 100, w = parseFloat(wt);
      if (h > 0 && w > 0) onChange((w / (h * h)).toFixed(1));
    }
  }, [ht, wt, mode]);
  const n = parseFloat(val);
  const ranges = [[-Infinity,16],[16,18.5],[18.5,25],[25,27.5],[27.5,30],[30,35],[35,40],[40,Infinity]];
  const labels = ["Крайнее истощение","Дефицит массы","Норма ✓","Небольшой избыток","Избыток","Ожирение I","Ожирение II","Ожирение III"];
  const colors = ["#818cf8","#60a5fa","#4ade80","#a3e635","#fbbf24","#f97316","#ef4444","#f43f5e"];
  const idx = ranges.findIndex(([lo, hi]) => n >= lo && n < hi);
  const info = n > 0 && idx >= 0 ? [labels[idx], colors[idx]] : null;
  const s = { padding:"10px 14px", background:"#0f172a", border:"1px solid #334155", borderRadius:8, color:"#f1f5f9", fontSize:15, fontFamily:"inherit" };
  return (
    <div>
      <div style={{ display:"flex", gap:8, marginBottom:8 }}>
        {[["hw","Рост + Вес"],["bmi","Ввести ИМТ напрямую"]].map(([m,l]) => (
          <button key={m} onClick={() => setMode(m)} style={{ padding:"4px 14px", borderRadius:20, border:"none", cursor:"pointer", fontSize:13, background:mode===m?"#2563eb":"#1e293b", color:mode===m?"#fff":"#94a3b8", fontFamily:"inherit" }}>{l}</button>
        ))}
      </div>
      {mode === "bmi"
        ? <input type="number" step="0.1" placeholder="например 22.5" value={val||""} onChange={e => onChange(e.target.value)} style={{...s, width:"100%", boxSizing:"border-box"}} />
        : <div style={{display:"flex", gap:8}}>
            <input type="number" placeholder="Рост (см)" value={ht} onChange={e => setHt(e.target.value)} style={{...s, flex:1}} />
            <input type="number" placeholder="Вес (кг)"  value={wt} onChange={e => setWt(e.target.value)} style={{...s, flex:1}} />
          </div>
      }
      {info && <div style={{marginTop:6, fontSize:13, color:info[1], fontWeight:600}}>ИМТ {n.toFixed(1)} — {info[0]}</div>}
    </div>
  );
}

